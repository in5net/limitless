export const csc = (x: number): number => 1 / Math.sin(x);
export const sec = (x: number): number => 1 / Math.cos(x);
export const cot = (x: number): number => 1 / Math.tan(x);

export const acsc = (x: number): number => 1 / Math.asin(x);
export const asec = (x: number): number => 1 / Math.acos(x);
export const acot = (x: number): number => 1 / Math.atan(x);

export const csch = (x: number): number => 1 / Math.sinh(x);
export const sech = (x: number): number => 1 / Math.cosh(x);
export const coth = (x: number): number => 1 / Math.tanh(x);

export const acsch = (x: number): number => 1 / Math.asinh(x);
export const asech = (x: number): number => 1 / Math.acosh(x);
export const acoth = (x: number): number => 1 / Math.atanh(x);
